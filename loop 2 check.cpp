#include<stdio.h>
int main()
{
	int i,j;
	int list[5]={3,4,5,6,7};
	for(i=0;i<5;i=i+1)
	{
		for(j=0;j<5;j++)
		{
			printf("%d",list[i]);
		}
		printf("\n");
	}
	return 0;
}




